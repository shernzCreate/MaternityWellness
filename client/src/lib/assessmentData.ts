/**
 * Get interpretation of Edinburgh Postnatal Depression Scale (EPDS) score
 * EPDS score range is 0-30
 * 0-8: Low likelihood of depression
 * 9-12: Possible depression
 * 13-30: Probable depression
 * Score of 1 or more on question 10 indicates possible risk of self-harm
 */
export function getEpdsInterpretation(score: number): { severity: string; description: string; color: string } {
  if (score <= 8) {
    return {
      severity: "Low Risk",
      description: "Your score suggests a low likelihood of depression. Continue to monitor your feelings and reach out if symptoms develop.",
      color: "bg-success"
    };
  } else if (score <= 12) {
    return {
      severity: "Moderate Risk",
      description: "Your score suggests possible depression. Consider discussing your feelings with a healthcare provider.",
      color: "bg-warning"
    };
  } else {
    return {
      severity: "High Risk",
      description: "Your score suggests probable depression. It's important to speak with a healthcare provider about your symptoms soon.",
      color: "bg-destructive"
    };
  }
}

/**
 * Get interpretation of Patient Health Questionnaire (PHQ-9) score
 * PHQ-9 score range is 0-27
 * 0-4: Minimal depression
 * 5-9: Mild depression
 * 10-14: Moderate depression
 * 15-19: Moderately severe depression
 * 20-27: Severe depression
 * Score of 1 or more on question 9 indicates possible risk of self-harm
 */
export function getPhq9Interpretation(score: number): { severity: string; description: string; color: string } {
  if (score <= 4) {
    return {
      severity: "Minimal",
      description: "Your score suggests minimal depression symptoms. Continue self-monitoring and self-care.",
      color: "bg-success"
    };
  } else if (score <= 9) {
    return {
      severity: "Mild",
      description: "Your score suggests mild depression. Consider watchful waiting, repeating PHQ-9 at follow-up, and using self-care resources.",
      color: "bg-success-light"
    };
  } else if (score <= 14) {
    return {
      severity: "Moderate",
      description: "Your score suggests moderate depression. Consider counseling, follow-up, and/or pharmacotherapy based on clinical judgment.",
      color: "bg-warning"
    };
  } else if (score <= 19) {
    return {
      severity: "Moderately Severe",
      description: "Your score suggests moderately severe depression. Consider active treatment with pharmacotherapy and/or psychotherapy.",
      color: "bg-warning-dark"
    };
  } else {
    return {
      severity: "Severe",
      description: "Your score suggests severe depression. Immediate initiation of pharmacotherapy and, if severe or with psychosis, expedited referral to a mental health specialist for psychotherapy and/or collaborative management.",
      color: "bg-destructive"
    };
  }
}